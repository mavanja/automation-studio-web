let currentLang = localStorage.getItem('as_language') || 'de'

export function t(key, params) {
  const str = TRANSLATIONS[currentLang]?.[key] || TRANSLATIONS['de']?.[key] || key
  if (!params) return str
  return Object.entries(params).reduce((s, [k, v]) => s.replace(`{${k}}`, v), str)
}

export function setLang(lang) {
  currentLang = lang
  localStorage.setItem('as_language', lang)
}

export function getLang() {
  return currentLang
}
